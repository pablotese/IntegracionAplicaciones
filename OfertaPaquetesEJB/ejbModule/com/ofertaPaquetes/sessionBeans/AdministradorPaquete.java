	private String getFechaString(Date fecha) {
		
		String date = "";
		String month = "";
		String day = "";
		if(fecha.getMonth() < 10)
			month = "0"+fecha.getMonth();
		if(fecha.getDay() < 10)
			month = "0"+fecha.getDay();
		
		date +=  fecha.getYear() + month + day;
		
		return date;
    }

